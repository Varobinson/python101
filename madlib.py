#Prompt the user for the missing words to a Madlib sentence using
# the input function. You can make up your own Madlib sentence, 
# but here's an example:

#showing user madlib
print("____(name)____'s favorite subject in school is ____(subject)____.")
#asking user to fill in information
user_name = input('what is your name? ')
favorite_subject = input('What is your favorite subject? ')

#printing result of filling info in madlib
print(f"{user_name}'s favorite subject in school is {favorite_subject}.")
